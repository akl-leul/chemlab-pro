// Helper function to get element by atomic number
export function getElementByNumber(atomicNumber: number) {
  return elementData.find((element) => element.number === atomicNumber)
}

// Helper function to get element by symbol
export function getElementBySymbol(symbol: string) {
  return elementData.find((element) => element.symbol === symbol)
}

// Helper function to get elements by category
export function getElementsByCategory(category: string) {
  return elementData.filter((element) => element.category === category)
}

// Helper function to get elements by period
export function getElementsByPeriod(period: number) {
  return elementData.filter((element) => element.period === period)
}

// Helper function to get elements by group
export function getElementsByGroup(group: number) {
  return elementData.filter((element) => element.group === group)
}
